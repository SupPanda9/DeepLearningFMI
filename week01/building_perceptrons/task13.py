# „Механизъм за внимание“ (attention mechanoisms).


# 17. What is the key point about GPT-3?
# GPT-3 е разработен като голям езиков модел с огромно количество данни и
# параметри. Ключовото, което проявява GPT-3, е, че има способности, за
# които системата не е била проектирана - прави някои логически изводи.
# Системата обаче не мисли, а прави предположения точно като една система за 
# автокорект, но в пъти по-сложна.

# 18. Is machine learning more efficient at learning than humans? Why?
# Не. Хората са по-успешни в ученето, защото не се нуждаят от милиарди
# скрейпнати статии от интернет, за да могат да мислят и да правят изводи.
# Освен това тези изводи по-често от тези на системите са верни. 

# 19. What is "the bitter truth"?
# Успехът в AI идва с много данни и много изчислителна мощ, дори това да
# не е най-добрият метод в научните среди.

# 20. What is one reason why ChatGPT is not conscious?
# Не мисли като хората. Не си дава сметка кой отговор ще е най-правилен, а 
# просто връща думи, чиято вероятност е най-висока. Не много по-различно от
# автокорект. Макар да не е точно ясно определено какво е съзнание, то ChatGPT
# определено не го притежава.
